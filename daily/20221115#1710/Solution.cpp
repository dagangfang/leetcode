class Solution {
public:
    int maximumUnits(vector<vector<int>>& boxTypes, int truckSize) {
        sort(boxTypes.begin(), boxTypes.end(), [](auto& a, auto& b) {return (a[1] >= b[1]);});
        int ret = 0;
        for (int i = 0;i < boxTypes.size();++i)
        {
            auto& a = boxTypes[i];
            if (truckSize > a[0])
            {
                truckSize -= a[0];
                ret += a[0] * a[1];
            }
            else
            {
                ret += truckSize * a[1];
                break;
            }
        }
        return ret;
    }
};


